package Sept.sept9.array;

public class array003 {

    public static void main(String[] args) {

        String [] fruits = new String [2] ;

        fruits [0] = "Orange" ;

        fruits [1] = "Apple" ;

        System.out.println(fruits.length);


        for (int i = 0; i < 2; i++) {

            System.out.println(fruits[i]);

        }


    }
}
