package Sept.sept3.switchdemo;

public class Lab003 {

    public static void main(String[] args) {

   String browser = "chrome";


   switch(browser) {

       case "chrome":

           System.out.println("code to start chrome browser");

           break;

       case "firefox":

           System.out.println("code to start firefox browser");

           break;

       default:

           System.out.println("invalid browser");

   }

    }

}
