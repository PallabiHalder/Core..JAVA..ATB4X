package Sept.sept3.forloop;

public class Lab004 {

    public static void main(String[] args) {


        boolean b= true ;

        for (;b;){                      // here 'b' means condition is true.

            System.out.println("Hi");  // it will print 'Hi' infinite times as condition is true.
                                       //  it will not give output if condition is false.
        }
    }
}
